sub complete_symbol {
  my($w) = @_;
  my @res = ();
  foreach my $t (@tmp){
    if($t =~ /$w/){
      push @res, $t;
    }
  }
  if($#res == 0){
    return "$res[0] ";
  }elsif($#res == -1){
    return $w;
  }else{ # multiple words completed
    my $w1 = $res[0];
    my $c = 0;
    my $t = 1;
    for($c = 0; $c <= length($w1); $c++){
      my @x = ();
      for my $word (@res){
        if($c > length($word)){
          push @x, "xx";
        }else{
          push @x, substr($word, $c, 1);
        }
      }
      my $c1 = $x[0];
      foreach my $c2 (@x){
        if($c1 ne $c2){
          $t = 0;
          last;
        }
        $c1 = $c2;
      }
      last if(!$t);
    }
    return substr($w1,0,$c);
  }
